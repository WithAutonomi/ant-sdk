std::vector<uint8_t> base64_decode(std::string_view encoded) {
    if (encoded.empty()) return {};

    // Strip trailing padding for length calculation.
    size_t padding = 0;
    if (encoded.size() >= 1 && encoded[encoded.size() - 1] == '=') ++padding;
    if (encoded.size() >= 2 && encoded[encoded.size() - 2] == '=') ++padding;

    size_t out_len = (encoded.size() / 4) * 3 - padding;
    std::vector<uint8_t> out;
    out.reserve(out_len);

    for (size_t i = 0; i < encoded.size(); i += 4) {
        uint32_t sextet[4] = {0, 0, 0, 0};
        for (int j = 0; j < 4; ++j) {
            if (i + j < encoded.size() && encoded[i + j] != '=') {
                uint8_t v = kDecodeTable[static_cast<uint8_t>(encoded[i + j])];
                if (v == 0xFF) {
                    throw std::runtime_error("invalid base64 character");
                }
                sextet[j] = v;
            }
        }
        uint32_t triple = (sextet[0] << 18) | (sextet[1] << 12) | (sextet[2] << 6) | sextet[3];

        if (out.size() < out_len) out.push_back(static_cast<uint8_t>((triple >> 16) & 0xFF));
        if (out.size() < out_len) out.push_back(static_cast<uint8_t>((triple >> 8) & 0xFF));
        if (out.size() < out_len) out.push_back(static_cast<uint8_t>(triple & 0xFF));
    }

    return out;
}

